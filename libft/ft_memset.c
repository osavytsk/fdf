#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*r;

	i = 0;
	r = b;
	while (i < len)
	{
		*r = (unsigned char)c;
		r++;
		i++;
	}
	return (b);
}
