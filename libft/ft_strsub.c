#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*substr;

	if (!s)
		return (NULL);
	if ((substr = ft_strnew(len)) != NULL)
		ft_strncpy(substr, s + start, len);
	return (substr);
}
