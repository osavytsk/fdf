#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	char	*head;

	if (!s || (str = ft_strnew(ft_strlen(s))) == NULL || !f)
		return (NULL);
	head = str;
	while (*s)
	{
		*str = (*f)(*s);
		str++;
		s++;
	}
	return (head);
}
